/**
 * \class APoint : objet point en 2D
 */ 
public class APoint {
    
    //<!coordonnées cartésiennes du point
    double x, y;
    
    /**
   * \fn APoint(double ax, double ay) : constructeur APoint
   * 
   * @param double ax : abscisse du point
   * @param double ay : ordonnée du point
   */ 
    public APoint(double ax, double ay){
        x = ax;
        y = ay;
    }    
}

